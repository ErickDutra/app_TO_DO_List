class Task {
  final int id;
  final String titulo;
  final bool day;
  final bool week;
  final bool month;
  final bool year;
  final String data;
  final String description;

  Task({
    required this.id,
    required this.titulo,
    required this.day,
    required this.week,
    required this.month,
    required this.year,
    required this.data,
    required this.description,
  });
}
